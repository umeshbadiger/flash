package io.javabrains;

public class MathUtils {
	
	public int add(int a, int b) {
		return a+b;
	}
	
	public double computeCircleArea(int radius) {
		return Math.PI * radius * radius ;
	}
	
	public int devide(int a, int b) {
		return a/b;
	}
	 
	public int multiply(int a, int b) {
		return a*b;
	}
}
